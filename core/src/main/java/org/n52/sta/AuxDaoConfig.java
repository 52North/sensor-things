package org.n52.sta;

import com.zaxxer.hikari.HikariDataSource;
import org.hibernate.boot.model.TypeContributor;
import org.hibernate.jpa.boot.internal.EntityManagerFactoryBuilderImpl;
import org.hibernate.jpa.boot.spi.TypeContributorList;
import org.hibernate.type.BasicType;
import org.n52.hibernate.type.SmallBooleanType;
import org.n52.sta.data.repositories.sta.AuxiliaryMessageBusRepositoryImpl;
import org.springframework.beans.factory.annotation.Qualifier;
import org.springframework.beans.factory.annotation.Value;
import org.springframework.boot.autoconfigure.jdbc.DataSourceProperties;
import org.springframework.boot.autoconfigure.orm.jpa.JpaProperties;
import org.springframework.boot.context.properties.ConfigurationProperties;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;
import org.springframework.data.jpa.repository.config.EnableJpaRepositories;
import org.springframework.orm.jpa.JpaTransactionManager;
import org.springframework.orm.jpa.LocalContainerEntityManagerFactoryBean;
import org.springframework.orm.jpa.vendor.HibernateJpaVendorAdapter;
import org.springframework.transaction.PlatformTransactionManager;
import org.springframework.util.StringUtils;

import javax.persistence.EntityManagerFactory;
import javax.sql.DataSource;
import java.util.Arrays;
import java.util.HashMap;
import java.util.Map;

/**
 * @author <a href="mailto:j.speckamp@52north.org">Jan Speckamp</a>
 */
@Configuration
@EnableJpaRepositories(repositoryBaseClass = AuxiliaryMessageBusRepositoryImpl.class,
                       entityManagerFactoryRef = "auxiliaryEntityManagerFactory",
                       transactionManagerRef = "auxiliaryTransactionManager",
                       basePackages = {"org.n52.sta.data.repositories.aux"})
public class AuxDaoConfig {

    @Value("${database.jpa.persistence-location}")
    private String persistenceXmlLocation;

    @Bean
    @ConfigurationProperties(prefix = "spring.datasource.hikari")
    HikariDataSource auxiliaryDataSource(DataSourceProperties properties) {
        HikariDataSource dataSource = properties.initializeDataSourceBuilder().type(HikariDataSource.class).build();
        if (StringUtils.hasText(properties.getName())) {
            dataSource.setPoolName(properties.getName());
        }
        dataSource.setPoolName("aux-pool");
        dataSource.setMaximumPoolSize(1);
        return dataSource;
    }

    @Bean
    public EntityManagerFactory auxiliaryEntityManagerFactory(@Qualifier("auxiliaryDataSource") DataSource datasource,
                                                              JpaProperties properties) {
        LocalContainerEntityManagerFactoryBean emf = new LocalContainerEntityManagerFactoryBean();
        emf.setJpaVendorAdapter(new HibernateJpaVendorAdapter());
        emf.setJpaPropertyMap(addCustomTypes(properties));
        emf.setPersistenceXmlLocation(persistenceXmlLocation);
        emf.setDataSource(datasource);
        emf.afterPropertiesSet();
        return emf.getNativeEntityManagerFactory();
    }

    /**
     * Used for internal detached Transactions, e.g. for persisting FormatEntity/OfferingEntity
     *
     * @param emf EntityManagerFactory to be used for creating the EMs
     * @return PlatformTransactionManager to be used
     */
    @Bean
    public PlatformTransactionManager auxiliaryTransactionManager(
        @Qualifier("auxiliaryEntityManagerFactory") EntityManagerFactory emf) {
        JpaTransactionManager transactionManager = new JpaTransactionManager();
        transactionManager.setEntityManagerFactory(emf);
        return transactionManager;
    }

    private Map<String, Object> addCustomTypes(JpaProperties jpaProperties) {
        Map<String, Object> properties = new HashMap<>(jpaProperties.getProperties());
        properties.put(EntityManagerFactoryBuilderImpl.TYPE_CONTRIBUTORS, createTypeContributorsList());
        return properties;
    }

    private TypeContributorList createTypeContributorsList() {
        return () -> Arrays.asList(toTypeContributor(SmallBooleanType.INSTANCE, "small_boolean"));
    }

    private <T extends BasicType> TypeContributor toTypeContributor(T type, String... keys) {
        return (typeContributions, serviceRegistry) -> typeContributions.contributeType(type, keys);
    }

}
